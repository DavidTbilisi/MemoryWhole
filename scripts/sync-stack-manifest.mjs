#!/usr/bin/env node
/**
 * Writes src/data/stack-manifest.generated.js — module hub metadata for Stack Library UI.
 *   npm run sync:stack-manifest
 *
 * Set ATLAS_DOC_BASE for clickable doc links (e.g. raw GitHub theSystem folder or deployed atlas URL).
 */

import { writeFileSync, mkdirSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const outFile = join(__dirname, '..', 'src', 'data', 'stack-manifest.generated.js')

const atlasBase = (process.env.ATLAS_DOC_BASE || 'https://raw.githubusercontent.com/DavidTbilisi/LearningSystem/main/theSystem').replace(/\/$/, '')

const modules = [
  { id: 'map', title: 'Map & path', tags: ['#map', '#onboarding'], docs: ['SKILL.md', 'onboarding-path.md'], decks: [{ id: 'stackfund', name: 'Stack fundamentals' }] },
  { id: 'comprehension', title: 'Comprehension', tags: ['#comprehension'], docs: ['comprehension-protocol.md', 'confusion-triage.md'], decks: [] },
  { id: 'concepts', title: 'Concepts (NEDF)', tags: ['#nedf'], docs: ['concept-encoding.md'], decks: [] },
  { id: 'relations', title: 'Relations / graphs', tags: ['#cast', '#georgian'], docs: ['cast-system.md', 'georgian-system.md'], decks: [{ id: 'cast', name: 'CAST edges' }, { id: 'castrev', name: 'CAST reverse' }] },
  { id: 'numbers', title: 'Numbers', tags: ['#major', '#pao', '#sem3', '#binary', '#hex'], docs: ['major-system.md', 'pao.md', 'sem3-full.md', 'binary-hex.md', 'encoding-examples.md'], decks: [{ id: 'major', name: 'Major' }, { id: 'binary', name: 'Binary 4-bit' }, { id: 'hex', name: 'Hex' }] },
  { id: 'procedures', title: 'Procedures', tags: ['#procedure', '#retrieval'], docs: ['procedure-encoding.md', 'retrieval-protocol.md'], decks: [] },
  { id: 'maintenance', title: 'Maintenance', tags: ['#collision', '#measurement', '#metacognitive'], docs: ['collisions.md', 'measurement-framework.md', 'metacognitive-checklist.md'], decks: [] },
  { id: 'patterns', title: 'Heuristics / patterns', tags: ['#heuristic', '#domain', '#triz'], docs: ['heuristic-palace.md', 'domain-patterns.md', 'triz.md'], decks: [] },
]

function docUrl(name) {
  return `${atlasBase}/${encodeURIComponent(name)}`
}

const enriched = modules.map((m) => ({
  ...m,
  docLinks: m.docs.map((d) => ({ label: d, href: docUrl(d) })),
}))

const body = `/* eslint-disable */
// AUTO-GENERATED by scripts/sync-stack-manifest.mjs — Regenerate: npm run sync:stack-manifest

export const STACK_MANIFEST_VERSION = 1
export const STACK_MANIFEST_GENERATED_AT = ${JSON.stringify(new Date().toISOString())}
export const STACK_MANIFEST_ATLAS_BASE = ${JSON.stringify(atlasBase)}
export const STACK_MANIFEST_MODULES = ${JSON.stringify(enriched, null, 2)}
`

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(outFile, body, 'utf8')
console.log('Wrote', outFile)
